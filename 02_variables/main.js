// Variables

/* Initialize variables with var (we don't do that anymore) */
var color = 'red';
console.log(color);

/* Initialize variable with let */
let age = 30;
console.log(age);

/* Declare variable */
let age2;
console.log(age2);

/* Assign variable */
age2 = 20;
console.log(age2);

/* Re-assign variable */
age2 = 25;
console.log(age2);

/* lowerCamelCase */
let lowerCamelCase = true;
console.log(lowerCamelCase);

